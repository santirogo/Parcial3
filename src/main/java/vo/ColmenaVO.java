public class ColmenaVO {
    private int poblacion;
    private boolean presenciaReina;
    private boolean produccionMiel;
    private int panelesCera;
    private int panelesAlimento;
    private int panelesCria;
    private int panelesVacios;

    public int getPoblacion() {
        return poblacion;
    }

    public void setPoblacion(int poblacion) {
        this.poblacion = poblacion;
    }

    public boolean isPresenciaReina() {
        return presenciaReina;
    }

    public void setPresenciaReina(boolean presenciaReina) {
        this.presenciaReina = presenciaReina;
    }

    public boolean isProduccionMiel() {
        return produccionMiel;
    }

    public void setProduccionMiel(boolean produccionMiel) {
        this.produccionMiel = produccionMiel;
    }

    public int getPanelesCera() {
        return panelesCera;
    }

    public void setPanelesCera(int panelesCera) {
        this.panelesCera = panelesCera;
    }

    public int getPanelesAlimento() {
        return panelesAlimento;
    }

    public void setPanelesAlimento(int panelesAlimento) {
        this.panelesAlimento = panelesAlimento;
    }

    public int getPanelesCria() {
        return panelesCria;
    }

    public void setPanelesCria(int panelesCria) {
        this.panelesCria = panelesCria;
    }

    public int getPanelesVacios() {
        return panelesVacios;
    }

    public void setPanelesVacios(int panelesVacios) {
        this.panelesVacios = panelesVacios;
    }
    
    
}
